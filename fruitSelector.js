let fruit = "banana";

switch (fruit.toLowerCase()) { 
  case "apple":
    alert("Apples are red or green.");
    break;
  case "banana":
    alert("Bananas are yellow and sweet.");
    break;
  case "orange":
    alert("Oranges are juicy and orange!");
    break;
  default:
    alert("I don't know about that fruit!");
}
